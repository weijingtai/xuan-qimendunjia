  static DateTime getPreviousZhengShouDongZhi(DateTime dateTime) {
    // return findClosestDate(ZhengShouDongZhi, dateTime);
    int tmpNumber = (dateTime.millisecondsSinceEpoch + CONSTANT) ~/ toMinutes;
    int resultNumber = findClosestNumber(zhengShouInMintes, tmpNumber);
    int resultMilliseconds = resultNumber * toMinutes - CONSTANT;
    return DateTime.fromMillisecondsSinceEpoch(resultMilliseconds);
  }

  static DateTime findClosestDate(List<DateTime> dates, DateTime target) {
    int left = 0;
    int right = dates.length - 1;

    while (left < right) {
      int mid = left + (right - left) ~/ 2;

      if (dates[mid].isBefore(target)) {
        left = mid + 1;
      } else {
        right = mid;
      }
    }

    // 检查最接近的两个日期
    if (left == 0) {
      return dates[0];
    }
    if (left == dates.length) {
      return dates[dates.length - 1];
    }

    DateTime before = dates[left - 1];
    DateTime after = dates[left];
    return (target.difference(before).abs() < target.difference(after).abs())
        ? before
        : after;
  }

  static int findClosestNumber(List<int> arr, int target) {
    int left = 0;
    int right = arr.length - 1;
    int result = 0;

    while (left <= right) {
      int mid = left + (right - left) ~/ 2;

      if (arr[mid] <= target) {
        result = arr[mid];
        left = mid + 1;
      } else {
        right = mid - 1;
      }
    }

    return result;
  }
